def setup_test_environment():
    """Clean up and prepare test environment."""
    print("=" * 60)
    print("BRONZE TIER VERIFICATION TEST")
    print("=" * 60)
    
    # Clean up folders
    folders_to_clean = ['Needs_Action', 'Plans', 'Pending_Approval', 'Approved', 'Done']
    for folder in folders_to_clean:
        folder_path = VAULT_PATH / folder
        if folder_path.exists():
            for f in folder_path.iterdir():
                if f.is_file():
                    f.unlink()
            print(f"[OK] Cleaned {folder}/ folder")
    
    # Clean Logs
    logs_dir = VAULT_PATH / 'Logs'
    if logs_dir.exists():
        for f in logs_dir.iterdir():
            if f.is_file() and f.suffix == '.json':
                f.unlink()
        print(f"[OK] Cleaned Logs/ folder")
    
    print()

