def test_3_approval_workflow(plan_file, task_file):
    """Test 3: Approval Workflow"""
    print("TEST 3: Approval Workflow")
    print("-" * 40)
    
    # Read plan and check for keywords
    content = plan_file.read_text().lower()
    APPROVAL_KEYWORDS = ["send", "email", "payment", "post", "delete", "publish", "share"]
    
    # Test with NO approval keywords (should auto-complete)
    needs_approval = any(keyword in content for keyword in APPROVAL_KEYWORDS)
    print(f"[OK] Plan contains approval keywords: {needs_approval}")
    
    if not needs_approval:
        print("[OK] No approval required - will auto-complete")
    else:
        print("[OK] Approval required - will create request")
    
    # Now test WITH approval keywords
    print("\n--- Testing WITH approval keywords ---")
    
    # Create a plan that requires approval
    plans_dir = VAULT_PATH / 'Plans'
    email_plan_file = plans_dir / f"PLAN_email_test_{datetime.now().strftime('%Y%m%d_%H%M%S')}.md"
    email_plan_content = f"""---
type: plan
created: {datetime.now().isoformat()}
status: pending
source_file: email_task.md
category: email
---

# Email Processing Plan

## Objective
Send email response to client inquiry.

## Action Items
- [ ] Draft email reply
- [ ] Send to client@example.com
- [ ] Log communication

---
*Generated by AI Employee v1.0.0*
"""
    email_plan_file.write_text(email_plan_content)
    print(f"[OK] Created email plan: {email_plan_file.name}")
    
    # Check for approval keywords
    email_content = email_plan_content.lower()
    needs_approval = any(keyword in email_content for keyword in APPROVAL_KEYWORDS)
    print(f"[OK] Email plan contains approval keywords: {needs_approval}")
    
    if needs_approval:
        # Create approval request
        from skills.create_approval_request import create_approval_request
        
        result = create_approval_request(
            vault_path=str(VAULT_PATH),
            action_type='email_send',
            action_details={
                'plan_file': email_plan_file.name,
                'task_file': 'email_task.md',
                'action_summary': 'Send email response to client'
            },
            priority='medium'
        )
        
        if result['success']:
            approval_file = Path(result['file_path'])
            print(f"[OK] Created approval request: {approval_file.name}")
            print("[PASS] TEST 3 PASSED: Approval workflow working\n")
            return approval_file, email_plan_file
        else:
            print(f"[FAIL] Failed to create approval request: {result.get('error')}")
            return None, email_plan_file
    
    return None, email_plan_file

